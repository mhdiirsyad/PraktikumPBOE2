    private void jButtonInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertActionPerformed
        // TODO add your handling code here:
        String nama = jTextFieldNama.getText();
        if(nama.isEmpty()){
            JOptionPane.showMessageDialog(this, "Nama tidak boleh kosong", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            service.add(nama);
            loadMahasiswa();
            jTextFieldNama.setText("");
        }catch (SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error add Mahasiswa", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonInsertActionPerformed

    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        // TODO add your handling code here:
        String idstr = jTextFieldIdEdit.getText();
        String nama = jTextFieldNameEdit.getText();
        if(idstr.isEmpty() || nama.isEmpty()){
            JOptionPane.showMessageDialog(this, "ID dan Nama tidak boleh kosong", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            int Id = Integer.parseInt(idstr);
            Mahasiswa mhs = new Mahasiswa();
            mhs.setId(Id);
            mhs.setNama(nama);
            service.update(mhs);
            loadMahasiswa();
            jTextFieldIdEdit.setText("");
            jTextFieldNameEdit.setText("");
            jTextFieldIdDelete.setText("");
            jTextFieldNameDelete.setText("");
        }catch (NumberFormatException nex){
            JOptionPane.showMessageDialog(this, "ID harus angka", "Type Error", JOptionPane.ERROR_MESSAGE);
        }catch (SQLException ex){
            Logger.getLogger(JFrameUtama.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        String idstr = jTextFieldIdDelete.getText();
        if(idstr.isEmpty()){
            JOptionPane.showMessageDialog(this, "ID tidak boleh kosong", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            int Id = Integer.parseInt(idstr);
            service.delete(Id);
            loadMahasiswa();
            jTextFieldIdDelete.setText("");
            jTextFieldNameDelete.setText("");
            jTextFieldIdEdit.setText("");
            jTextFieldNameEdit.setText("");
        }catch (NumberFormatException nex){
            JOptionPane.showMessageDialog(this, "ID harus angka", "Type Error", JOptionPane.ERROR_MESSAGE);
        }catch (SQLException ex){
            Logger.getLogger(JFrameUtama.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_jButtonDeleteActionPerformed

