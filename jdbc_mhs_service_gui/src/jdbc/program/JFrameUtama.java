    private void loadMahasiswa() throws SQLException{
        DefaultListModel<String> dlmID = new DefaultListModel<>();
        DefaultListModel<String> dlmNama = new DefaultListModel<>();
        List<Mahasiswa> listmhs = service.getAll();
        for(Mahasiswa mhs : listmhs){
            dlmID.addElement(String.valueOf(mhs.getId()));
            dlmNama.addElement(mhs.getNama());
        }
        jListID.setModel(dlmID);
        jListNama.setModel(dlmNama);
    }
    
    private void updateTextFieldsFromList(){
        int index = jListID.getSelectedIndex();
        if(index != -1){
            String selectedID = jListID.getModel().getElementAt(index);
            String selectedNama = jListNama.getModel().getElementAt(index);
            
            jTextFieldIdEdit.setText(selectedID);
            jTextFieldNameEdit.setText(selectedNama);
            jTextFieldIdDelete.setText(selectedID);
            jTextFieldNameDelete.setText(selectedNama);
        }
    }
